//测试三子棋游戏
#include<stdio.h>
#include"game.h"
void menu()
{
	printf("******************************\n");
	printf("******1.play   0. exit   *****\n");
	printf("******************************\n");
}
//游戏的整个实现
void game()
{
	char ret = 0;
	//创建数组--存放走出的棋盘信息
	char board[ROW][COL] = {0};//全部空格
	//初始化棋盘
	InitBoard(board, ROW, COL); 
	//打印棋盘
	displayBoard(board,ROW,COL);
	//下棋
	while (1)
	{
		//玩家下棋
		playermove(board,ROW,COL);
		displayBoard(board, ROW, COL);
		ret=is_win(board,ROW,COL);
		if (ret != 'c')
		{
			break;
		}
		//判断玩家是否赢
		
		//电脑下棋
		computermove(board, ROW, COL);
		displayBoard(board, ROW, COL);
		ret=is_win(board,ROW,COL);
		if (ret != 'c')
		{
			break;
		}
		//判断电脑是否赢
	}
	if (ret == '*')
	{
		printf("玩家赢\n");
	}
	else if (ret == '#')
	{
		printf("电脑赢\n");
	}
	else
	{
		printf("平局\n");
	}


}
void text()
{
	int input = 0;
	srand((unsigned int )time(NULL));
	do
	{

		menu();
		printf("请选择：");
			scanf_s("%d", &input);
			switch (input)
			{
			case 1:
				game();
				break;
			case 0:
				printf("退出游戏\n");
				break;
			default:
				printf("选择错误，请重新选择\n");
				break;

			}
		
	} while (input);
}
int main()
{
	text();
	return 0;
}